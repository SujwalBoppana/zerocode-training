package de.zerocode.util;

public class Book {
	public String bookName;
	public String authourName;
	public int noOfPages;
	public int price;

	public Book(String bookName, String authourName, int noOfPages, int price) {
		this.bookName = bookName;
		this.authourName = authourName;
		this.noOfPages = noOfPages;
		this.price = price;

	}

}
