package de.zerocode.util;

public class BasicCalculator {

	public int getAdd(int first, int second) {
		return first + second;
	}

	public int getSubtract(int first, int second) {
		return first - second;
	}

	public int getMulti(int first, int second) {
		return first * second;
	}

	public int getDivision(int first, int second) {
		return first / second;
	}

}
